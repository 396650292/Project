package com.scme.pojo;

public class Train {
	String id;
	String trainer;//培训人
	String train_theme;//培训主题 
	String train_aim;//培训目标
	String train_time;//培训时间
	String train_address;//培训地址
	String attendee;//参加人员
	String train_remarks;//备注信息
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTrainer() {
		return trainer;
	}
	public void setTrainer(String trainer) {
		this.trainer = trainer;
	}
	public String getTrain_theme() {
		return train_theme;
	}
	public void setTrain_theme(String train_theme) {
		this.train_theme = train_theme;
	}
	public String getTrain_aim() {
		return train_aim;
	}
	public void setTrain_aim(String train_aim) {
		this.train_aim = train_aim;
	}
	public String getTrain_time() {
		return train_time;
	}
	public void setTrain_time(String train_time) {
		this.train_time = train_time;
	}
	public String getTrain_address() {
		return train_address;
	}
	public void setTrain_address(String train_address) {
		this.train_address = train_address;
	}
	public String getAttendee() {
		return attendee;
	}
	public void setAttendee(String attendee) {
		this.attendee = attendee;
	}
	public String getTrain_remarks() {
		return train_remarks;
	}
	public void setTrain_remarks(String train_remarks) {
		this.train_remarks = train_remarks;
	}
	
	

}
